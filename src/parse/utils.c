#include "../../cub3d.h"

int	ft_strchr_cub(const char *s)
{
	int	i;

	i = 0;
	if (!s)
		return (0);
	while (s[i])
		i++;
	if (i < 4)
		return (0);
	if (s[i - 4] == '.' && s[i - 3] == 'c' && s[i - 2] == 'u' && s[i
			- 1] == 'b')
		return (1);
	return (0);
}